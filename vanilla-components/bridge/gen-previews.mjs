#!/usr/bin/env node
// @ts-check
// Generate .design-sync/previews/<Pascal>.tsx from each component's
// <name>.preview.js `variants` — so the design-sync cards share ONE source of
// truth with the vanilla catalogue instead of being hand-authored React. One
// scene per variant: `export const <Variant> = () => <Pascal {...props} />`, which
// the adapter's React shim (declarative / tooltip / dialog / menu) renders.
//
// A component opted out of design-sync (bridge `{ skip: true }`) or without a
// `.preview.js` is skipped, and any stale `.tsx` with no matching component is
// pruned, so the previews dir always mirrors the synced set.
//
//   node bridge/gen-previews.mjs   (run before a re-sync, next to emit-adapter.mjs)
import { readdir, readFile, writeFile, stat, rm } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { discoverComponents } from "./shared.mjs";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = dirname(HERE);
const COMPONENTS = join(ROOT, "components");
const OUT = join(ROOT, ".design-sync", "previews");

/** A variant key → a valid PascalCase React component name. */
const sceneName = (key) => {
  const p = (key.charAt(0).toUpperCase() + key.slice(1)).replace(/[^A-Za-z0-9]/g, "");
  return /^[0-9]/.test(p) ? "V" + p : p;
};
/** A prop → a JSX attribute. `true` is shorthand; everything else is a JS expression
 *  (safe for strings with quotes/newlines, and for arrays/objects like menu items). */
const attr = (k, v) => (v === true ? k : `${k}={${JSON.stringify(v)}}`);
const exists = (p) => stat(p).then(() => true, () => false);

const kept = new Set();
let n = 0;
// discoverComponents() is the SHARED, authoritative synced set (skip-filtered,
// throws on a missing/broken bridge) — the same list emit-adapter builds, so a
// generated .tsx can't drift from the adapter's exports. A component with no
// `.preview.js` is in the adapter but simply gets no card.
for (const c of await discoverComponents(ROOT)) {
  const previewPath = join(COMPONENTS, c.name, `${c.name}.preview.js`);
  if (!(await exists(previewPath))) continue;
  const variants = (await import(pathToFileURL(previewPath).href)).default?.variants;
  if (!variants || typeof variants !== "object") continue;

  const scenes = Object.entries(variants)
    .map(([key, props]) => `export const ${sceneName(key)} = () => <${c.Pascal} ${Object.entries(props).map(([k, v]) => attr(k, v)).join(" ")} />;`)
    .join("\n\n");
  const body =
    `// GENERATED by bridge/gen-previews.mjs from ${c.name}.preview.js — do not edit by hand.\n` +
    `import { ${c.Pascal} } from "vanilla-components";\n\n${scenes}\n`;
  await writeFile(join(OUT, `${c.Pascal}.tsx`), body);
  kept.add(`${c.Pascal}.tsx`);
  n++;
}

// prune our own stale output only — verify the GENERATED banner so a hand-added
// .tsx dropped into the dir is never collateral.
for (const f of await readdir(OUT)) {
  if (!f.endsWith(".tsx") || kept.has(f)) continue;
  if ((await readFile(join(OUT, f), "utf8")).startsWith("// GENERATED by bridge/gen-previews.mjs")) {
    await rm(join(OUT, f));
    console.log(`  pruned stale ${f}`);
  }
}
console.log(`generated ${n} preview .tsx -> .design-sync/previews/`);
